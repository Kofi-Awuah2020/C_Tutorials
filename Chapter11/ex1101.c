#include <stdio.h>

int main()
{
    //Using the Math Operator '++' in a For loop
    for (int x =0; x < 10; x++)
    {
        printf("Get off the lawn, you kids.\n");
    }
    putchar('\n');
  

    //Using the Math Operator '++' in a While loop

    /*  Local variable definition */
    int y = 0;

    /*  While loop execution */
    while (y < 10)
    {
        printf("Get off the lawn, you kids.\n");
        y++;
    }
    return(0);
}